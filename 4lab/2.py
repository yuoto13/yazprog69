def center_text_in_window(s, w):
    # Функция центрирует строку s в окне определенной ширины w.
    if len(s) >= w:
        # Если длина строки s больше или равна ширине окна w,
        # возвращаем исходную строку s.
        return s
    # Вычисляем количество пробелов, необходимых для центрирования строки s в окне.
    spaces = (w - len(s)) // 2
    # Центрируем строку путем добавления пробелов слева от строки s.
    centered_text = ' ' * spaces + s
    # Возвращаем центрированный текст.
    return centered_text
# Пользовательский ввод строки и ширины окна.
text = input("Введите строку для центрирования: ")
width = int(input("Введите ширину окна: "))

# Вызываем функцию center_text_in_window() с введенными значениями и выводим результат.
result = center_text_in_window(text, width)
print(result)
