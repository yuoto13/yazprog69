def selection_sort(alist): # сортировка выбором
    for i in range(0, len(alist) - 1):  # Проходим по каждому элементу списка, кроме последнего
        smallest = i  # Считаем текущий элемент наименьшим
        for j in range(i + 1, len(alist)):  # Проходим по оставшимся элементам списка
            if alist[j] < alist[smallest]:  # Если находим элемент меньше текущего наименьшего
                smallest = j  # Обновляем индекс наименьшего элемента
        alist[i], alist[smallest] = alist[smallest], alist[i]  # Меняем местами текущий элемент и наименьший

# Получение списка от пользователя
alist = input('Введите список чисел: ').split()  # Ввод списка чисел, разделённых пробелами
alist = [int(x) for x in alist]  # Преобразование введённых значений в целые числа
selection_sort(alist)  # Сортировка списка методом выбора
print('Отсортированный список: ', end='')  # Вывод текста перед отсортированным списком
print(alist)  # Вывод отсортированного списка